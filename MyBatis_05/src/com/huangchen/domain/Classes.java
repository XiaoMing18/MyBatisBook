package com.huangchen.domain;
/**
 * 定义class表对应的实体表
 * @author huangchen
 *
 */
public class Classes {
	//定义实体类的属性，与class表中的字段对应
	private int id;//id == > c_id
	private String name;//name == >c_name
    private Teacher teacher;
	public Teacher getTeacher() {
		return teacher;
	}
	public void setTeacher(Teacher teacher) {
		this.teacher = teacher;
	}
	/**
	 * class表中有一个teacher_id字段，所以在Classes类中定义一个teacher属性
	 * 用于维护teacher和classes之间的一对一关系，通过这个teacher属性就可以知道这个班级是由哪个老师负责的
	 * @return
	 */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Classes [id=" + id + ", name=" + name + ", teacher=" + teacher + ", getTeacher()=" + getTeacher()
				+ ", getId()=" + getId() + ", getName()=" + getName() + ", getClass()=" + getClass() + ", hashCode()="
				+ hashCode() + ", toString()=" + super.toString() + "]";
	}
	

}
